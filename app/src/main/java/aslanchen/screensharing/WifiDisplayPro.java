package aslanchen.screensharing;

public class WifiDisplayPro {

    private String DeviceAddress;
    private String DeviceAlias;
    private String DeviceName;
    private String DeviceType;
    private String GroupCapability;

    public String getDeviceAddress() {
        return DeviceAddress;
    }

    public void setDeviceAddress(String deviceAddress) {
        DeviceAddress = deviceAddress;
    }

    public String getDeviceAlias() {
        return DeviceAlias;
    }

    public void setDeviceAlias(String deviceAlias) {
        DeviceAlias = deviceAlias;
    }

    public String getDeviceName() {
        return DeviceName;
    }

    public void setDeviceName(String deviceName) {
        DeviceName = deviceName;
    }

    public String getDeviceType() {
        return DeviceType;
    }

    public void setDeviceType(String deviceType) {
        DeviceType = deviceType;
    }

    public String getGroupCapability() {
        return GroupCapability;
    }

    public void setGroupCapability(String groupCapability) {
        GroupCapability = groupCapability;
    }

}
